# __main__ test harness
# ---------------------------------------------------------------------------

if __name__ == "__main__":
    logging.basicConfig(
        level=logging.INFO,
        format="%(asctime)s [%(levelname)s] %(name)s - %(message)s",
    )

    from utils.inventory_checker import InventoryError
    from tools.kubernetes.get_pods import GetPodsTool

    print("=" * 60)
    print("  GetLogsTool — test harness")
    print("=" * 60)

    try:
        config = load_inventory()
    except InventoryError as exc:
        logger.error("Inventory validation failed [%s]: %s", exc.code.value, exc)
        sys.exit(1)

    conn = _build_connection(config)
    try:
        log_tool = GetLogsTool.from_inventory(config, conn)
        pod_tool = GetPodsTool.from_inventory(config, conn)

        # ----------------------------------------------------------------
        # Test 1: Input validation — invalid pod name must return FAILURE
        # without executing any SSH command.
        # ----------------------------------------------------------------
        print("\n--- Test 1: invalid pod name rejected before SSH ---")
        result = log_tool.get_pod_logs("INVALID POD NAME!")
        assert result.status == ResultStatus.FAILURE, \
            f"Expected FAILURE, got {result.status}"
        assert result.exit_code == -1
        print(f"  PASS: status={result.status.value}, stderr='{result.stderr}'")

        # ----------------------------------------------------------------
        # Test 2: Invalid --since value rejected.
        # ----------------------------------------------------------------
        print("\n--- Test 2: invalid --since value rejected ---")
        result = log_tool.get_logs_since("some-pod", since="1day")
        assert result.status == ResultStatus.FAILURE
        print(f"  PASS: status={result.status.value}, stderr='{result.stderr}'")

        # ----------------------------------------------------------------
        # Test 3: tail_lines clamped to _MAX_TAIL_LINES.
        # ----------------------------------------------------------------
        print("\n--- Test 3: tail_lines clamped to MAX ---")

        # Discover the first running pod in the target namespace.
        pods_result = pod_tool.get_pods()
        if not pods_result.success or not pods_result.stdout.strip():
            print("  SKIP: no pods found in namespace, skipping live log tests.")
        else:
            # Parse first data line (skip header).
            lines = [l for l in pods_result.stdout.splitlines() if l and not l.startswith("NAME")]
            if lines:
                pod_name = lines[0].split()[0]
                print(f"  Using pod: {pod_name}")

                # Test 4: get_pod_logs with a valid pod.
                print(f"\n--- Test 4: get_pod_logs({pod_name}, tail=20) ---")
                result = log_tool.get_pod_logs(pod_name, tail_lines=20)
                tag = f"[{result.status.value.upper()}]"
                print(f"  {tag} ({result.duration_seconds:.2f}s)")
                print(result.stdout[:500] or result.stderr or "(no output)")

                # Test 5: get_logs_since with valid pod.
                print(f"\n--- Test 5: get_logs_since({pod_name}, since='5m') ---")
                result = log_tool.get_logs_since(pod_name, since="5m")
                tag = f"[{result.status.value.upper()}]"
                print(f"  {tag} ({result.duration_seconds:.2f}s)")
                print(result.stdout[:500] or result.stderr or "(no output)")

                # Test 6: get_previous_logs (may return error if no previous container).
                print(f"\n--- Test 6: get_previous_logs({pod_name}) ---")
                result = log_tool.get_previous_logs(pod_name, tail_lines=20)
                tag = f"[{result.status.value.upper()}]"
                print(f"  {tag} ({result.duration_seconds:.2f}s)")
                print(result.stdout[:500] or result.stderr or "(no previous container — expected)")

        print("\n" + "=" * 60)
        print("  All GetLogsTool tests complete.")
        print("=" * 60)

    finally:
        conn.disconnect()
